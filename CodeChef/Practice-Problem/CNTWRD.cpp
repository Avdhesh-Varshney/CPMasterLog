// Counting Words
// Problem Code - CNTWRD

// https://www.codechef.com/problems/CNTWRD

// Solution:
#include <iostream>
using namespace std;
int main() {
	// your code goes here
	int t;
	cin >> t;
	for(int i = 0; i < t; i++) {
	    int n, m;
	    cin >> n >> m;
	    cout << n*m << endl;
	}
	return 0;
}